    public boolean checkTaiKhoan() {
        String mauMaSp = "[a-zA-Z0-9]{1,10}";
        String mauDangNhap = "[a-zA-Z0-9]{1,50}";
        String mauEmail = "[a-zA-Z0-9]{4,50}@gmail.com";
        if (txtMaNguoiDung_DangKy.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập Mã Nhân Viên");
            txtMaNguoiDung_DangKy.requestFocus();
            return false;
        }
        if (!txtMaNguoiDung_DangKy.getText().matches(mauMaSp)) {
            JOptionPane.showMessageDialog(this, "Mã Nhân Viên Không Hợp Lệ");
            txtMaNguoiDung_DangKy.requestFocus();
            return false;
        }
        if (txtTenNhanVien_DangKy.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập Tên Nhân Viên");
            txtTenNhanVien_DangKy.requestFocus();
            return false;
        }
        if (txtEmail_NhanVien_DangKy.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập Email");
            txtEmail_NhanVien_DangKy.requestFocus();
            return false;
        }
        if (!txtEmail_NhanVien_DangKy.getText().matches(mauEmail)) {
            JOptionPane.showMessageDialog(this, "Email không đúng định dạng");
            txtEmail_NhanVien_DangKy.requestFocus();
            return false;
        }

        if (txtTenDangNhap.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập Tên Đăng Nhập");
            txtTenDangNhap.requestFocus();
            return false;
        }
        if (!txtTenDangNhap.getText().matches(mauDangNhap)) {
            JOptionPane.showMessageDialog(this, "Tên Đăng Nhập không có ký tự đặc biệt");
            txtTenDangNhap.requestFocus();
            return false;
        }
        if (txtMatKhau.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập Mật Khẩu");
            txtMatKhau.requestFocus();
            return false;
        }
        if (txtNhapLaiMatKhau.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập lại Mật Khẩu");
            txtNhapLaiMatKhau.requestFocus();
            return false;
        }
        if (!txtMatKhau.getText().equals(txtNhapLaiMatKhau.getText())) {
            JOptionPane.showMessageDialog(this, "Mật Khẩu không khớp, vui lòng nhập lại");
            txtMatKhau.setText("");
            txtNhapLaiMatKhau.setText("");
            txtMatKhau.requestFocus();
            return false;
        }
        return true;
    }
