    public boolean checkSanPham() {
        String mauMaSp = "[a-zA-Z0-9]{1,10}";
        String mauDonGia = "[0-9,]{1,50}";
        String mauSoLuong = "[0-9]{1,11}";
        if (txtMaGiay_QLG.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập Mã Sản Phẩm");
            txtMaGiay_QLG.requestFocus();
            return false;
        }
        if (!txtMaGiay_QLG.getText().matches(mauMaSp)) {
            JOptionPane.showMessageDialog(this, "Mã Sản Phẩm Không Hợp Lệ");
            txtMaGiay_QLG.requestFocus();
            return false;
        }
        if (txtTenGiay_QLG.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập Tên Sản Phẩm");
            txtTenGiay_QLG.requestFocus();
            return false;
        }
        if (txtSoLuong_QLG.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập Số Lượng");
            txtSoLuong_QLG.requestFocus();
            return false;
        }
        if (!txtSoLuong_QLG.getText().matches(mauSoLuong)) {
            JOptionPane.showMessageDialog(this, "Số Lượng không đúng định dạng");
            txtSoLuong_QLG.requestFocus();
            return false;
        }
        int soLuong = Integer.parseInt(txtSoLuong_QLG.getText());
        if (soLuong < 1) {
            JOptionPane.showMessageDialog(this, "Số Lượng phải lớn hơn 0");
            txtSoLuong_QLG.requestFocus();
            return false;
        }
        if (txtDonGia_QLG.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập Đơn Giá");
            txtDonGia_QLG.requestFocus();
            return false;
        }
        if (!txtDonGia_QLG.getText().matches(mauDonGia)) {
            JOptionPane.showMessageDialog(this, "Đơn Giá không đúng định dạng");
            txtDonGia_QLG.requestFocus();
            return false;
        }
        int donGia = Integer.parseInt(txtDonGia_QLG.getText().replace(",", ""));
        if (donGia < 1000) {
            JOptionPane.showMessageDialog(this, "Đơn giá phải lớn hơn 1,000");
            txtDonGia_QLG.requestFocus();
            return false;
        }
        return true;
    }

