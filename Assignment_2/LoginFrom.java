    public boolean check() {
        String userName = txtUserName.getText();
        String passWord = txtPassWord.getText();

        if (txtUserName.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Chưa nhập tên đăng nhập");
            //txtUserName.setBackground(Color.yellow);
            txtUserName.setBackground(new java.awt.Color(255, 0, 0, 128));
            txtUserName.requestFocus();
            txtPassWord.setText("");
            return false;
        } else if (txtPassWord.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Chưa nhập mật khẩu");
            txtPassWord.setBackground(new java.awt.Color(255, 0, 0, 128));
            txtUserName.setBackground(new java.awt.Color(0, 0, 0, 1));
            txtPassWord.requestFocus();
            return false;

        }
        return true;
    }
