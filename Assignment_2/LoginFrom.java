    private void btnDangNhapMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnDangNhapMouseClicked
        if (check()) {
            String userName = txtUserName.getText();
            String passWord = txtPassWord.getText();
            try {
                KetNoiCSDL();
                String sql = "SELECT Quyen,HoTen FROM NguoiDung WHERE TenDangNhap = ? AND MatKhau = ?";
                PreparedStatement cauLenh = ketNoi.prepareStatement(sql);
                cauLenh.setString(1, txtUserName.getText());
                cauLenh.setString(2, txtPassWord.getText());
                ResultSet ketQua = cauLenh.executeQuery();

                if (ketQua.next() == true) {
                    String quyen = ketQua.getString(1);
                    String hoTen = ketQua.getString(2);
                    FileWriter fw = null;
                    try {
                        fw = new FileWriter("src\\Ten\\TenNV.txt", false);
                        fw.write(hoTen);
                        fw.write("\n");
                        System.out.println("Lưu thành công");
                    } catch (Exception e) {
                        System.out.println("Lưu thất bại");
                    } finally {
                        try {
                            fw.close();
                        } catch (IOException e) {
                            System.out.println("Lỗi đóng file");
                        }
                    }
                    if (quyen.equals("Admin")) {
                        PhanMem qlkg = new PhanMem();
                        qlkg.setVisible(true);
                        dispose();
                    } else {
                        QuanLyHoaDon qlhd = new QuanLyHoaDon();
                        qlhd.setVisible(true);
                        dispose();
                    }
                } else {
                    JOptionPane.showMessageDialog(this, "Sai tên đăng nhập hoặc mật khẩu!");
                    txtUserName.setText("");
                    txtPassWord.setText("");
                    txtUserName.setBackground(new java.awt.Color(255, 0, 0, 128));
                    txtPassWord.setBackground(new java.awt.Color(0, 0, 0, 1));
                    txtUserName.requestFocus();
                }

            } catch (ClassNotFoundException ex) {
                Logger.getLogger(LoginFrom.class.getName()).log(Level.SEVERE, null, ex);
            } catch (SQLException ex) {
                Logger.getLogger(LoginFrom.class.getName()).log(Level.SEVERE, null, ex);
            }

        }
    }//GEN-LAST:event_btnDangNhapMouseClicked

